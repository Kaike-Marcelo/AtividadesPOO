    private void EnviarDadosBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarDadosBtnActionPerformed
        try {
            if (campoSenha.getText().equals(campoConfirmarSenha.getText())) {
                
                if (!campoSenha.getText().isBlank()) {

                    String nome = campoNome.getText();
                    String email = campoEmail.getText();
                    String senha = campoSenha.getText();

                    PessoaBO pessoaBo = new PessoaBO();
                    PessoaModel pessoaM = new PessoaModel();

                    pessoaM.setNome(nome);
                    pessoaM.setEmail(email);
                    pessoaM.setSenha(senha);

                    boolean confirmarInsersao = pessoaBo.incluir(pessoaM);

                    if (confirmarInsersao) {
                        JOptionPane.showMessageDialog(rootPane, "Sucesso! Dados Inseridos com sucesso.");
                        this.dispose();

                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Erro! Os campos 'Email' e 'Nome' não devem estar vazios.");
                    }
                    
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Erro! o campo 'Senha' não deve estar vazio.");
                }
                    
                
                
            } else {
                JOptionPane.showMessageDialog(null, "As senhas devem ser iguais", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EnviarDadosBtnActionPerformed

